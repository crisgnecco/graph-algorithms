
public class Color {

	private Integer valor;
	private Integer indice;

	public Color(Integer valor, Integer indice) {
		this.valor = valor;
		this.indice = indice;
	}

	public void setValor(Integer valor) {
		this.valor = valor;
	}

	public Integer getValor() {
		return valor;
	}

	public Integer getIndice() {
		return indice;
	}
}
